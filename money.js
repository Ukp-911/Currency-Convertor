let currcodeL = "inr";
let concodeL = "IN";
let currcodeR = "usd";
let concodeR = "US";

let dropdown = document.querySelectorAll("select");
for (let i of dropdown) {
    for (let j in countryList) {
        let el = document.createElement("option");
        el.setAttribute("value", countryList[j]);
        el.textContent = countryList[j];
        i.append(el);
    }
    i.addEventListener("change", (evt) => {
        let tar = evt.target;
        if (tar.className == "leftcoun") {
            concodeL = tar.value;
            let opt = concodeL;
            let key = Object.keys(countryList).find(k => countryList[k] === opt);
            currcodeL = key.toLowerCase();
        } else {
            concodeR = tar.value;
            let opt = concodeR;
            let key = Object.keys(countryList).find(k => countryList[k] === opt);
            currcodeR = key.toLowerCase();
        }
        flags(evt.target);
    })
}
const flags = (val) => {
    let imga = val.parentElement.querySelector("img");
    imga.src = `https://flagsapi.com/${val.value}/flat/64.png`;
}

const conversion = async () => {
    let code =  await fetch(`https://2024-03-06.currency-api.pages.dev/v1/currencies/${currcodeR}.json`);
    let value = await code.json();
    let inp = document.querySelector("#enter").value;
    let convert = inp*value[currcodeR][currcodeL];
    let res=document.querySelector(".result");
    res.textContent=`${inp} ${currcodeL.toUpperCase()} = ${convert} ${currcodeR.toUpperCase()}`;
}
let btn = document.querySelector(".exbtn");
btn.addEventListener("click", () => {
    conversion();
})